export enum Badge {
    // Performance Badges
    Immortal = 'Immortal',               // 0 deaths, >0 frags
    Rambo = 'Rambo',                     // 5+ kills in 60 seconds
    Unstoppable = 'Unstoppable',         // 10+ kill streak
    Flawless = 'Flawless',               // 10+ frags with 0 deaths
    Comeback = 'Comeback',               // Reverse 5+ frag deficit

    // Weapon Mastery Badges
    RifleKing = 'RifleKing',             // 80%+ kills with single weapon
    Arsenal = 'Arsenal',                 // 3+ different weapons used effectively

    // Team Badges
    Protector = 'Protector',             // High impact on teammates

    // Rare Badges
    Perfect = 'Perfect',                 // Win (most kills) with 0 deaths
    Blitz = 'Blitz',                     // 7+ kills in 30 seconds
}
